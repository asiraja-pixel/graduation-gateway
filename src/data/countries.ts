export const getNationalityByCountry = (country: string): CountryNationality | undefined => {
  return COUNTRIES_NATIONALITIES.find(
    c => c.country.toLowerCase() === country.toLowerCase()
  );
};

export const searchCountries = (query: string): CountryNationality[] => {
  const lowerQuery = query.toLowerCase();
  return COUNTRIES_NATIONALITIES.filter(
    c =>
      c.country.toLowerCase().includes(lowerQuery) ||
      c.nationality.toLowerCase().includes(lowerQuery) ||
      c.callingCode.includes(query)
  );
};
